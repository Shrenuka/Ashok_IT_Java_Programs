package Operators;

public class TernaryOpeartors {

	public static void main(String[] args) {
		
		int a=12, b=22;
		
		System.out.println(a>b ? "True":"false"); //either expression1 or expression2 will be printed according to condition
		System.out.println(a<b ? "true":"false");
		System.out.println(a>=b ? "true":"False");
		System.out.println(a<=b ? "True":"false");
		System.out.println(a==b ? "true":"false");
		System.out.println(a!=b ? "true":"false");
	}

}
